#include <iostream>
int main(int argc, char const *argv[])
{
    int value{5};
    std::cout << value << std::endl;
    // Addition in Compoung form
    value +=5;
    std::cout << value << std::endl;
    // Subtraction in Compoung form
    value -=5;
    std::cout << value << std::endl;
    // Multiplication in Compoung form
    value *=5;
    std::cout << value << std::endl;
    // Division in Compoung form
    value /=5;
    std::cout << value << std::endl;
    // for using compoung form 
    // just use the operator +, -, *, /
    // with = sign
    // for example:
    // +=, -=, *=, /=
    
    return 0;
}
